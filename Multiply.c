/* C equivalent code of Multiply() */
#include<stdio.h>
int main()
{
    int a, b, c;
    scanf("%d %d", &a, &b);     //Taking two numbers 'a' and 'b' as input from the user
    c = a*b;        //Storing the value of their product into a third variable 'c'
    printf("%d", c);        //Printing the value of the third variable
}