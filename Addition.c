/* C equivalent code of Addition() */
#include<stdio.h>
int main()
{
    int a, b, c;
    scanf("%d %d", &a, &b);     //Taking two numbers 'a' and 'b' as input from the user
    c = a+b;        //Storing the sum of these two numbers into a third variable 'c'
    printf("%d", c);        //Printing the value of the third variable
}